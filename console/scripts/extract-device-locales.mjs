#!/usr/bin/env node
/*
 * extract-device-locales.mjs — one-shot generator (kept for re-runs when the
 * console's device.* copy changes): bundles each console locale with esbuild,
 * slices out the subtree @jcloud/device-ui owns (full `device.*`, plus the
 * `run.*` keys PermissionCard/DeviceTimeline reference), and writes the
 * package's locale modules. The console locale files stay the source of
 * truth for the console itself; the package registers these bundles on hosts
 * (mobile) that don't ship console locales.
 */
import { execFileSync } from 'node:child_process';
import { mkdtempSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { pathToFileURL } from 'node:url';

// esbuild is a transitive (vite) dep — call its CLI from the pnpm store.
const esbuildBin = new URL(
  '../node_modules/.pnpm/esbuild@0.25.12/node_modules/esbuild/bin/esbuild',
  import.meta.url,
).pathname;

const LOCALES = ['en', 'zh-Hans', 'zh-Hant', 'ja', 'ko'];
const consoleSrc = new URL('../src/i18n/locales/', import.meta.url).pathname;
const outDir = new URL('../packages/device-ui/src/i18n/locales/', import.meta.url).pathname;

for (const lng of LOCALES) {
  const tmp = mkdtempSync(join(tmpdir(), 'locale-'));
  const outfile = join(tmp, 'locale.mjs');
  execFileSync(esbuildBin, [
    join(consoleSrc, `${lng}.ts`),
    '--bundle',
    '--format=esm',
    '--platform=node',
    `--outfile=${outfile}`,
    '--log-level=silent',
  ]);
  const mod = await import(pathToFileURL(outfile).href);
  const full = mod.default;
  const slice = {
    run: {
      toolStatusDone: full.run.toolStatusDone,
      toolStatus: full.run.toolStatus,
      unknownEvent: full.run.unknownEvent,
      permission: full.run.permission,
    },
    device: full.device,
  };
  const body = `// GENERATED by scripts/extract-device-locales.mjs from src/i18n/locales/${lng}.ts.\n` +
    `// Do not hand-edit; re-run the script after changing the console's device.* copy.\n` +
    `export default ${JSON.stringify(slice, null, 2)};\n`;
  writeFileSync(join(outDir, `${lng}.ts`), body);
  console.log(`wrote ${lng}.ts`);
}
